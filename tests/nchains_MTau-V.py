# read n-th bit:读出n位上的数为0或1
def read_bit(i,n):
     return (i&(1<<n))>>n
 
# flip n-th bit：反转第n位上的数为0或1
def flip_bit (i,n):
     return i^(1<<n)
 
#count_bit:读出i中有多少个1  
def count_bit(i):
    return bin(i).count("1")

#search_bit:将基矢标号
def search_i(basis, i):
    idex = 0
    for temp in basis:
        if temp == i:
           break
        idex = idex + 1
    return idex







